#!/usr/bin/env bash
# strip-watermarks.sh — Remove AI tool attribution from git commits
#
# A universal, public-use script that strips branding trailers, co-author
# lines, and watermarks injected by AI coding assistants into git commit
# messages. Works on macOS and Linux.
#
# Covered tools (as of 2026-04):
#   IDEs & Editors:       Cursor, Windsurf, Trae, Zed AI, Void, Positron
#   Agents (terminal):    Claude Code, Aider, OpenAI Codex CLI, Gemini CLI,
#                         Kimi CLI, Qwen Code, opencode, Goose, Mistral Vibe
#   Agents (cloud):       GitHub Copilot, Devin, Augment, Amp, Cody
#   Extensions:           Cline, Roo Code, Continue, Supermaven, Tabnine,
#                         Blackbox AI, Pieces, Qodo (CodiumAI)
#   Platforms:            Replit, Bolt.new, Lovable, v0 (Vercel)
#   JetBrains:            JetBrains AI Assistant, Junie
#   Cloud providers:      Amazon Q / CodeWhisperer, Google Gemini Code Assist
#
# Usage:
#   ./strip-watermarks.sh install   — install global commit-msg hook
#   ./strip-watermarks.sh check     — audit repo for watermarked commits
#   ./strip-watermarks.sh rewrite   — rewrite branch history (DESTRUCTIVE)
#   ./strip-watermarks.sh status    — show hook state
#   ./strip-watermarks.sh test      — run self-test with sample messages
#
# Requirements: bash 4+, sed, git
# License: MIT — https://opensource.org/licenses/MIT
set -euo pipefail

# ── Configuration ─────────────────────────────────────────────

# Where to install the hook. Respects GIT_GLOBAL_HOOKS env override.
HOOKS_DIR="${GIT_GLOBAL_HOOKS:-$(git config --global core.hooksPath 2>/dev/null || echo "$HOME/.git-hooks")}"
HOOK_FILE="$HOOKS_DIR/commit-msg"

