# ── Commands ──────────────────────────────────────────────────

cmd_install() {
    echo "Installing watermark-stripping commit-msg hook..."
    echo "  target: $HOOK_FILE"

    mkdir -p "$HOOKS_DIR"

    if [ -f "$HOOK_FILE" ]; then
        if grep -q "strip-watermarks" "$HOOK_FILE" 2>/dev/null; then
            echo "  hook exists — updating patterns in place"
        else
            # Preserve existing hook by appending chain
            echo "  existing hook found — backing up to $HOOK_FILE.bak"
            cp "$HOOK_FILE" "$HOOK_FILE.bak"
        fi
    fi

    write_hook "$HOOK_FILE"

    # Verify global hooksPath
    local current_path
    current_path=$(git config --global core.hooksPath 2>/dev/null || true)
    if [ -z "$current_path" ]; then
        echo ""
        echo "  NOTE: core.hooksPath not set. To activate globally, run:"
        echo "    git config --global core.hooksPath '$HOOKS_DIR'"
    elif [ "$current_path" != "$HOOKS_DIR" ]; then
        echo ""
        echo "  NOTE: core.hooksPath is '$current_path' (expected '$HOOKS_DIR')"
        echo "  The hook was written to $HOOK_FILE but may not be active."
    fi

    echo ""
    echo "  done. Future commits will be scrubbed across all repos."
    echo ""
    echo "  Covered tools ($(echo "${GREP_PATTERNS[@]}" | tr '|' '\n' | wc -l | tr -d ' ')+ patterns):"
    echo "    Cursor, Claude Code, GitHub Copilot, OpenAI Codex CLI, Aider,"
    echo "    Gemini CLI/Code Assist, Amazon Q, Windsurf, Codeium, JetBrains/Junie,"
    echo "    Trae, Cline, Roo Code, Continue, Devin, Augment, Amp, Sourcegraph/Cody,"
    echo "    Tabnine, Supermaven, Qodo, Blackbox AI, Pieces, Replit, Bolt, Lovable,"
    echo "    v0, OpenHands, Goose, Mistral Vibe, Kimi CLI, Qwen Code, Zed AI, opencode"
}

cmd_check() {
    echo "Auditing commits for AI watermarks..."
    echo ""

    local total=0
    local seen_hashes=""

    for pattern in "${GREP_PATTERNS[@]}"; do
        while IFS= read -r line; do
            [ -z "$line" ] && continue
            local hash="${line%% *}"
            # Deduplicate
            if echo "$seen_hashes" | grep -qF "$hash"; then
                continue
            fi
            seen_hashes="$seen_hashes $hash"
            echo "  $line"
            total=$((total + 1))
        done < <(git log --all --extended-regexp --format="%H %ai %s" --grep="$pattern" 2>/dev/null)
    done

    echo ""
    if [ "$total" -eq 0 ]; then
        echo "Clean. No AI watermarks found."
    else
        echo "Found $total watermarked commit(s)."
        echo "Run '$0 rewrite' to clean them (DESTRUCTIVE — rewrites history)."
    fi
}

cmd_rewrite() {
    echo "╔══════════════════════════════════════════════════════════════╗"
    echo "║  WARNING: This rewrites git history using filter-branch.   ║"
    echo "║  Only use on branches NOT pushed to a shared remote.       ║"
    echo "║  A safety tag will be created for rollback.                ║"
    echo "╚══════════════════════════════════════════════════════════════╝"
    echo ""
    echo "Press Enter to continue, Ctrl+C to abort..."
    read -r

    local tag="pre-watermark-strip-$(date +%Y%m%d-%H%M%S)"
    echo "Creating safety tag: $tag"
    git tag "$tag"

    # Build the sed command from generate_sed_args, stripping comments
    local sed_script
    sed_script=$(generate_sed_args | grep -v '^#' | grep -v '^$' | tr '\n' ';')

    git filter-branch --force --msg-filter "
        sed $(generate_sed_args | grep -v '^#' | grep -v '^$' | sed \"s|^|-e '|;s|$|'|\" | tr '\n' ' ') \
        | sed -e :a -e '/^\n*\$/{\$d;N;ba' -e '}'
    " -- --all

    echo ""
    echo "History rewritten. Verify with: $0 check"
    echo "Rollback with: git reset --hard $tag"
}

cmd_status() {
    echo "=== AI Watermark Hook Status ==="
    echo ""
    echo "Global hooks path: $(git config --global core.hooksPath 2>/dev/null || echo '(not set)')"
    echo "Expected hook:     $HOOK_FILE"

    if [ -f "$HOOK_FILE" ]; then
        if grep -q "strip-watermarks\|Strip AI watermarks" "$HOOK_FILE" 2>/dev/null; then
            echo "Status:            ACTIVE"
            local pattern_count
            pattern_count=$(grep -c "^    -e " "$HOOK_FILE" 2>/dev/null || echo 0)
            echo "Patterns:          $pattern_count sed rules"
        else
            echo "Status:            HOOK EXISTS (no watermark stripping detected)"
        fi
    else
        echo "Status:            NOT INSTALLED"
        echo ""
        echo "Run '$0 install' to set up."
    fi

    echo ""
    echo "Covered: Cursor, Claude Code, Copilot, Codex CLI, Aider, Gemini,"
    echo "  Amazon Q, Windsurf, Codeium, JetBrains, Junie, Trae, Cline,"
    echo "  Roo Code, Continue, Devin, Augment, Amp, Cody, Tabnine,"
    echo "  Supermaven, Qodo, Blackbox, Pieces, Replit, Bolt, Lovable,"
    echo "  v0, OpenHands, Goose, Mistral, Kimi, Qwen, Zed, opencode"
}

cmd_test() {
    echo "=== Self-test: verifying watermark stripping ==="
    echo ""

    local pass=0
    local fail=0
    local tmpdir
    tmpdir=$(mktemp -d)

    run_test() {
        local name="$1"
        local input="$2"
        local expected="$3"

        local tmpfile="$tmpdir/msg"
        printf '%s\n' "$input" > "$tmpfile"

        # Simulate the hook's sed commands using write_hook output
        local hook="$tmpdir/hook"
        write_hook "$hook"
        bash "$hook" "$tmpfile"

        local actual
        actual=$(cat "$tmpfile")

        if [ "$actual" = "$expected" ]; then
            echo "  PASS  $name"
            pass=$((pass + 1))
        else
            echo "  FAIL  $name"
            echo "    expected: $(echo "$expected" | head -1)"
            echo "    got:      $(echo "$actual" | head -1)"
            fail=$((fail + 1))
        fi
    }

    run_test "Cursor trailer" \
        "$(printf 'fix(auth): validate JWT\n\nMade-with: Cursor')" \
        "fix(auth): validate JWT"

    run_test "Claude co-author" \
        "$(printf 'feat(api): add endpoint\n\nCo-Authored-By: Claude Opus 4.6 <noreply@anthropic.com>')" \
        "feat(api): add endpoint"

