    run_test "Windsurf trailer" \
        "$(printf 'fix: style\n\nMade-with: Windsurf')" \
        "fix: style"

    run_test "Generated-by Codex" \
        "$(printf 'feat: init\n\nGenerated-by: Codex CLI')" \
        "feat: init"

    run_test "Amazon Q co-author" \
        "$(printf 'fix: lambda\n\nCo-Authored-By: Amazon Q <noreply@amazon.com>')" \
        "fix: lambda"

    rm -rf "$tmpdir"

    echo ""
    echo "Results: $pass passed, $fail failed"
    [ "$fail" -eq 0 ] && return 0 || return 1
}

